export class Calculatrice {
  public addition(nbre1: number, nbre2: number): number {
    return nbre1 + nbre2;
  }
  public soustraction(nbre1: number, nbre2: number): number {
    return nbre1 - nbre2;
  }
  public multiplication(nbre1: number, nbre2: number): number {
    return nbre1 * nbre2;
  }
  public division(nbre1: number, nbre2: number): number {
    return nbre1 / nbre2;
  }
}
